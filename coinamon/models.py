# coding: utf-8

from sqlalchemy import Column, String
from .db import Model


class User(Model):
    name = Column(String)
    fullname = Column(String)
    password = Column(String)

    def __repr__(self):
        return "<User(name='%s', fullname='%s', password='%s')>" % (
            self.name, self.fullname, self.password)
